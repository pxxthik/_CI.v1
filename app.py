def add(a, b):
    return a + b


def subtract(a, b):
    return a - b


def multiply(a, b):
    return a * b


def divide(a, b):
    return a / b


if __name__ == "__main__":
    sum = add(1, 2)
    diff = subtract(1, 2)
    product = multiply(1, 2)
    quotient = divide(1, 2)

    print(sum, diff, product, quotient)
